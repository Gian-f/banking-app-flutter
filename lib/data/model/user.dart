class User {
  final int id_account;
  final String name;
  final String email;
  final double created_at;
  final double updated_at;
  final String contact_number;

  User({
    required this.id_account,
    required this.name,
    required this.email,
    required this.created_at,
    required this.updated_at,
    required this.contact_number,
  });
}
